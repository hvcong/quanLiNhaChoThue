$("#modal-room").on("show.bs.modal", function (e) {
    var btn = $(e.relatedTarget);
    var recipient = btn.data("whatever");

    console.log(recipient);

    var modal = $(this);
    var defaultText = "Chưa xác định";
    modal.find(".modal-title").text(recipient?.title || defaultText);
    modal.find(".address-text").text(recipient?.address || defaultText);
    modal.find(".floor-text").text(recipient?.floor || defaultText);
    modal.find(".room-text").text(recipient?.room || defaultText);
    modal.find(".price-text").text(recipient?.price || defaultText);
    modal.find(".size-text").text(recipient?.size || defaultText);
});
